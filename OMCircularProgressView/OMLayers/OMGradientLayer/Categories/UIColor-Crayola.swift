public static func crayolaColorWithRed:(CGFloat)red ,green:(CGFloat)green ,blue:(CGFloat)blue ,alpha:(CGFloat)alpha
{
	static var cache: NSCache? = nil;

	if(nil == cache)
	{
		cache = NSCache()

		cache.setName(“UIColor+Crayola")
	}

	let cacheKey = NSString(format:“%.2f%.2f%.2f%.2f", red, green, blue, alpha)

	let color = cache.objectForKey:cacheKey)

	if(nil == color)
	{
		color = UIColor(red:red ,green:green ,blue:blue ,alpha:alpha)

		cache.setObject(color, forKey:cacheKey)
	}

	return color;
}


