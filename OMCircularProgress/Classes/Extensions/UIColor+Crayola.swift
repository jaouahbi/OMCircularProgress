    class var cache: NSCache<NSString, UIColor> {
        return NSCache()
    }
    // constructor copy
    convenience init(color: UIColor) {
        var red:CGFloat = 0
        var green:CGFloat = 0
        var blue:CGFloat = 0
        var alpha:CGFloat = 1.0
        color.getRed(&red, green: &green, blue: &blue, alpha: &alpha)
        self.init(red:red , green:green, blue:blue, alpha:alpha);
    }
    
    convenience init(crayolared:CGFloat , green:CGFloat , blue:CGFloat, alpha:CGFloat) {
        let cacheKey = UIColor.cacheKeyWithRed(red:crayolared , green:green , blue:blue, alpha:alpha);
        let color = UIColor.cache.object(forKey: cacheKey);
        if(color != nil) {
            UIColor.cache.setObject(color!,forKey:cacheKey)
            self.init(color:color!)
        } else {
            self.init(red:crayolared , green:green , blue:blue, alpha:alpha)
        }
    }
    
    static func cacheKeyWithRed(red:CGFloat , green:CGFloat , blue:CGFloat, alpha:CGFloat) -> NSString {
        return NSString(format:"%.2f%.2f%.2f%.2f", red, green, blue, alpha);
    }
